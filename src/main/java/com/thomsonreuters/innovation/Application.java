package com.thomsonreuters.innovation;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.boot.CommandLineRunner;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.context.annotation.Bean;

import java.util.UUID;

@SpringBootApplication
public class Application {

    private static final Logger log = LoggerFactory.getLogger(Application.class);

    public static void main(String[] args) {
        SpringApplication.run(Application.class);
    }

    @Bean
    public CommandLineRunner demo(CaseBeanRepository repository) {
        return (args) -> {
            // save a couple of customers
            String id = UUID.randomUUID().toString();
            repository.save(new Case(id, "Test Bean"));

            log.info("");

            // fetch an individual customer by ID
            Case aCase = repository.findOne(id);
            log.info("Case found with findOne():");
            log.info("--------------------------------");
            log.info(aCase.toString());
        };


    }
}
